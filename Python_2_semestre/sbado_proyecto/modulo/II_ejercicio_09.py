#condicional anidada
letra = input("Ingrese una letra : ")
if(letra=="a" or letra=="A") :
    print("Es la vocal a")
elif(letra=="e" or letra=="E") :
    print("Es la vocal e")
elif(letra=="i" or letra=="I") :
    print("Es la vocal i")
elif(letra=="o" or letra=="O") :
    print("Es la vocal o")
elif(letra=="u" or letra=="U") :
    print("Es la vocal u")
else :
    print("Es una consonante")